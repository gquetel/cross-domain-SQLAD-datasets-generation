from pathlib import Path


class DotDict(dict):
    def __getattr__(self, attr):
        try:
            return self[attr]
        except KeyError:
            raise AttributeError(f"Attribute {attr} not found")

    def __setattr__(self, attr, value):
        self[attr] = value


class ProjectPaths:
    def __init__(
        self,
        base_path: str,
    ):
        self.base_path = base_path
        self.subfolder = None

    def set_subfolder_output_path(self, subfolder: str):
        self.subfolder = subfolder

    @property
    def dataset_path(self) -> str:
        return f"{self.base_path}/../dataset.csv"

    @property
    def output_path(self) -> str:
        path = f"{self.base_path}/../output/results/"

        if self.subfolder:
            path += f"{self.subfolder}/"

        Path(path).mkdir(exist_ok=True, parents=True)
        return path

    @property
    def embeddings_path(self) -> str:
        path = f"{self.base_path}/../cache/embeddings/"
        Path(path).mkdir(exist_ok=True, parents=True)
        return path

    @property
    def logs_path(self) -> str:
        path = f"{self.base_path}../logs/"
        Path(path).mkdir(exist_ok=True, parents=True)
        return path

    @property
    def features_cache_path(self) -> str:
        path = f"{self.base_path}/../cache/features/"
        Path(path).mkdir(exist_ok=True, parents=True)
        return path


